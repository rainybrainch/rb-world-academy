export default function DailyTip() {
  const today = useMemo(() => {
    const d = new Date();
    return d.getDay();
  }, []);

  const tip = useMemo(() => {
    const idx = today % TIPS.length;
    return TIPS[idx];
  }, [today]);

  const [copied, setCopied] = useState(false);

  const copy = async () => {
    if (!navigator?.clipboard) return;
    await navigator.clipboard.writeText(tip.text);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <div
      className="rounded-2xl p-4 flex items-start gap-3"
      style={{
        background: 'rgba(91,200,232,0.08)',
        border: '1px solid rgba(91,200,232,0.2)',
      }}
    >
      <span className="text-3xl shrink-0" style={{ lineHeight: 1 }}>
        {tip.icon}
      </span>
      <div className="flex-1 min-w-0">
        <div
          className="text-xs font-bold tracking-widest mb-1"
          style={{ color: 'rgba(91,200,232,0.6)', fontFamily: "'Zen Maru Gothic', sans-serif" }}
        >
          TODAY'S TIP
        </div>
        <p
          className="text-sm leading-relaxed"
          style={{ color: 'rgba(26,26,46,0.7)', fontFamily: "'Zen Maru Gothic', sans-serif" }}
        >
          {tip.text}
        </p>
        <button
          type="button"
          onClick={copy}
          className="text-xs mt-2 px-2 py-1 rounded transition-colors"
          style={{
            background: copied ? 'rgba(76,175,125,0.2)' : 'rgba(91,200,232,0.1)',
            color: copied ? 'rgba(76,175,125,0.7)' : 'rgba(91,200,232,0.6)',
          }}
        >
          {copied ? '✓ コピーしました' : 'コピー'}
        </button>
      </div>
    </div>
  );
}
