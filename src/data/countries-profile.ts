// TypeScript の型チェックとインポート用
export type CountryId = keyof typeof countriesProfile;

// 全国リスト取得用ヘルパー関数
export const getAllCountries = (): CountryProfile[] => {
  return Object.values(countriesProfile);
};

// 特定国を ID で取得
export const getCountryProfile = (countryId: CountryId): CountryProfile | undefined => {
  return countriesProfile[countryId];
};

// 国リストを ID 配列で取得
export const getCountriesByIds = (ids: CountryId[]): CountryProfile[] => {
  return ids
    .map(id => countriesProfile[id])
    .filter((profile): profile is CountryProfile => profile !== undefined);
};
